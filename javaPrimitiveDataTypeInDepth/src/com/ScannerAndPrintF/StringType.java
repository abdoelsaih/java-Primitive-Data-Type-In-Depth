package com.ScannerAndPrintF;

public class StringType {

	public static void main(String[] args) {

		String name = "Micheal Jackson";
		System.out.println(name.charAt(4)); // this will print the letter number 4
		System.out.println(name.substring(0, 5)); // this to print the letters from 0 till 4
		System.out.println(name.contains("Micheal")); // this to search and answer by true or false
		System.out.println(name.isEmpty());// this is the text is empty and answer is true of false
		// from 0-5 = output is from 0 to 4
		// if i put one number it prints the full number

	}
}